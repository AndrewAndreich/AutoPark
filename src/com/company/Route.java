package com.company;

public class Route {
    private String number;
    private String lengthRoute;
    private String busAmount;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getLengthRoute() {
        return lengthRoute;
    }

    public void setLengthRoute(String lengthRoute) {
        this.lengthRoute = lengthRoute;
    }

    public String getBusAmount() {
        return busAmount;
    }

    public void setBusAmount(String busAmount) {
        this.busAmount = busAmount;
    }
}
